  <p style ="text-align:justify;">The Executive Mayor is charged with the overall responsibility of providing political leadership to the Rustenburg Local Municipality on behalf of the ruling party, the African National Congress. The Executive Mayor appoints ten councillors to the Mayoral Committee to assist him in delivering his political mandate. The members of the Mayoral Committee serve as heads in various portfolios in line with the Council-approved alternative service delivery organisational structure. Important tools have been put in place to ensure optimal decision-making processes, that is, the approved corporate system of delegation (which defines specific powers allocated to the Executive Mayor) and the Mayoral Committee system (to ensure an efficient committee system).</p>
  <p>The Council has also approved the city’s Five-year Strategic Plan. The Five-year Strategic</p>
  <p>Plan outlines the following five key strategic objectives to drive the city’s service delivery programme:
</p>
<ul>
<li>To provide quality basic services and infrastructure</li>
<li>To facilitate higher and shared economic growth and development</li>
<li>To fight poverty and build clean, healthy, safe and sustainable communities</li>
<li>To foster participatory democracy and Batho Pele principles through a caring</li>
<li>Accessible and accountable service</li>
<li>To ensure good governance, financial viability and optimal institutional transformation with the capacity to execute its mandate</li>
<li>To create a world class city where all communities enjoy a high quality of life.</li>
</ul>
<p style ="text-align:justify;">The Municipal Manager is responsible for the management of the administration. He is supported by a team of directors. Together with the Executive Mayor they have established the joint Office of the Executive Mayor and office of the Municipal Manager as a strong centre to ensure smooth management of the political and administrative interface.</p>

